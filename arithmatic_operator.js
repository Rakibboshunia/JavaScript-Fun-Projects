
// Arithmatic Operators in JavaScript

// Addition (+)

let a = 10;
let b = 5;

console.log("Addition: " + (a + b)); 

// Subtraction (-)

console.log("Subtraction: " + (a - b));

// Multiplication (*)

console.log("Multiplication: " + (a * b));

// Division (/)

console.log("Division: " + (a / b));

// Modulus (%)

console.log("Modulus: " + (a % b));  // Remainder of 10 divided by 5 is 0

// Exponentiation (**)

console.log("Exponentiation: " + (a ** b)); // 10^5 = 100000

// Increment (++)

let c = 10;
console.log("Pre-increment: " + (++c));  // Pre-increment

let d = 15;
console.log("Post-increment: " + (d++));  // Post-increment

// Decrement (--)

let e = 20;
console.log("Pre-decrement: " + (--e));  // Pre-decrement

let f = 25;
console.log("Post-decrement: " + (f--));  // Post-decrement
