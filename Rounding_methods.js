// Rounding Methods  

        let x = 7.6;
        let s;

        s = Math.ceil(x); // Rounds up
        document.write(s + "<br>");

        s = Math.floor(x); // Rounds down
        document.write(s + "<br>");

        s = Math.round(x); // Rounds to nearest integer
        document.write(s + "<br>");

        s = Math.trunc(x); // Removes decimal part
        document.write(s + "<br>");
